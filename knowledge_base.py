def get_all_topics() -> list[str]:
    """获取所有知识点名称列表"""
    topics = []
    for ch in KNOWLEDGE_TREE.values():
        for sec in ch["sections"].values():
            topics.extend(sec["topics"])
    return topics


def get_topics_by_section(section_id: str) -> list[str]:
    """根据小节编号获取知识点列表，如 '1.2' → ['因数与倍数', ...]"""
    ch_id = section_id.split(".")[0]
    ch = KNOWLEDGE_TREE.get(ch_id)
    if ch:
        sec = ch["sections"].get(section_id)
        if sec:
            return sec["topics"]
    return []


def get_exercises_by_topic(topic: str) -> list[dict]:
    """根据知识点名称获取练习题列表"""
    return EXERCISES.get(topic, [])


def get_concept(topic: str) -> dict | None:
    """根据知识点名称获取概念讲解"""
    return CONCEPTS.get(topic)


def get_section_title(section_id: str) -> str:
    """获取小节标题"""
    ch_id = section_id.split(".")[0]
    ch = KNOWLEDGE_TREE.get(ch_id)
    if ch:
        sec = ch["sections"].get(section_id)
        if sec:
            return sec["title"]
    return ""


def print_knowledge_tree():
    """打印完整知识树"""
    for ch_id, ch in KNOWLEDGE_TREE.items():
        print(f"\n第{ch_id}章  {ch['title']}")
        print("=" * 40)
        for sec_id, sec in ch["sections"].items():
            print(f"  {sec_id} {sec['title']}")
            for topic in sec["topics"]:
                has_concept = "+" if topic in CONCEPTS else " "
                has_exercises = f"x{len(EXERCISES[topic])}" if topic in EXERCISES else ""
                print(f"    {has_concept} - {topic}  {has_exercises}")



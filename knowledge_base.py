_BASE_DIR = os.path.dirname(os.path.abspath(__file__))
_IMPORT_FILE = os.path.join(_BASE_DIR, "data", "imported_exercises.json")


def _load_imported_exercises():
    """从 data/imported_exercises.json 加载外部导入的题目，合并到 EXERCISES。
    支持三种操作：
    - 新题目：追加到对应知识点
    - 覆盖内置题目：JSON 中与内置题目 ID 相同的条目会覆盖内置版本
    - 删除标记：JSON 中 _deleted 列表里的 ID 会被从题库中移除
    """
    if not os.path.exists(_IMPORT_FILE):
        return
    try:
        with open(_IMPORT_FILE, "r", encoding="utf-8") as f:
            imported = json.load(f)
    except (json.JSONDecodeError, OSError):
        return

    all_topics = set(get_all_topics())

    # 1) 处理删除标记：移除被标记删除的内置题目
    deleted_ids = set(imported.get("_deleted", []))
    if deleted_ids:
        for topic in list(EXERCISES.keys()):
            EXERCISES[topic] = [ex for ex in EXERCISES[topic]
                                if ex.get("id") not in deleted_ids]

    # 2) 建立内置题目的 ID → (topic, index) 索引，用于覆盖
    builtin_index = {}
    for topic, exs in EXERCISES.items():
        for i, ex in enumerate(exs):
            eid = ex.get("id")
            if eid:
                builtin_index[eid] = (topic, i)

    # 3) 合并外部题目（新增 + 覆盖）
    for topic, exs in imported.items():
        if topic.startswith("_"):
            continue  # 跳过 _deleted 等特殊键
        if topic not in all_topics:
            continue
        if topic not in EXERCISES:
            EXERCISES[topic] = []
        for ex in exs:
            eid = ex.get("id")
            if eid in deleted_ids:
                continue
            if eid in builtin_index:
                # 覆盖已有题目（内置或之前导入的）
                old_topic, old_idx = builtin_index[eid]
                EXERCISES[old_topic][old_idx] = ex
            else:
                EXERCISES[topic].append(ex)
                builtin_index[eid] = (topic, len(EXERCISES[topic]) - 1)


